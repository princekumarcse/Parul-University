# A program that sorts a list of numbers in assending or decending order

numbers = [5, 2, 9, 1, 7]

# Ascending order
numbers.sort()
print("Ascending order:", numbers)

# Descending order
numbers.sort(reverse=True)

# sort() → arranges numbers in ascending order (small to big)
# sort(reverse=True) → arranges in descending order (big to small)
print("Descending order:", numbers)